def heart_report(idx, out_dir):
    age, bp, chol, hr, oldpeak, outcome, risk, interp = HEART_CASES[idx]
    pt  = PATIENTS[idx]
    doc = DOCTORS[0]
    rid = ref_id("HRT")
    pdf = LabReport("CARDIAC BIOMARKER PANEL REPORT", rid, pt, doc, BLUE)

    pdf.add_page()
    pdf.report_title()
    pdf.patient_block()

    pdf.info_note(
        "Values extracted by MedicalAI: Age | Blood Pressure | Cholesterol | Heart Rate | ST Depression",
        (255, 248, 220), (120, 70, 0), (200, 150, 0)
    )

    pdf.section_heading("SAMPLE INFORMATION")
    pdf.set_font("Helvetica", "", 9)
    pdf.cell(65, 6, "  Sample Type: Venous Blood")
    pdf.cell(65, 6, "Collection: Fasting (12 hrs)")
    pdf.cell(52, 6, "Method: Automated Analyser", ln=True)
    pdf.ln(3)

    pdf.section_heading("CARDIAC BIOMARKER RESULTS")
    cols   = ["Test Name", "Result", "Unit", "Reference Range", "Method"]
    widths = [64, 22, 18, 46, 24]
    pdf.tbl_header(cols, widths)

    bp_flag   = "H" if bp    > 130 else None
    chol_flag = "H" if chol  > 200 else None
    hr_flag   = "H" if hr    > 100 else ("L" if hr < 60 else None)
    op_flag   = "H" if oldpeak > 1.0 else None

    pdf.tbl_row(["Age",                       str(age),        "Years", "-",          "-"],         widths)
    pdf.tbl_row(["Resting Blood Pressure",     str(bp),         "mmHg",  "90 - 120",   "Oscillo."],  widths, bp_flag)
    pdf.tbl_row(["Cholesterol (Total Serum)",  str(chol),       "mg/dL", "125 - 200",  "Enzymatic"], widths, chol_flag)
    pdf.tbl_row(["Max Heart Rate (Thalach)",   str(hr),         "bpm",   "60 - 100",   "ECG"],       widths, hr_flag)
    pdf.tbl_row(["ST Depression (Oldpeak)",    f"{oldpeak:.1f}","mm",    "0.0 - 1.0",  "ECG"],       widths, op_flag)

    pdf.ln(2)
    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(*GREY)
    pdf.cell(0, 5, "  H = High  |  L = Low  |  Rows shaded red indicate values outside reference range.", ln=True)
    pdf.set_text_color(*BLACK)

    pdf.interpretation(interp, risk)

    # structured data block (machine readable by pdfplumber)
    pdf.set_fill_color(248, 248, 252)
    pdf.set_draw_color(*BLUE)
    pdf.section_heading("STRUCTURED DATA  (Machine-Readable for Digital Health Records)")
    pdf.set_font("Courier", "", 9)
    pdf.set_fill_color(248, 248, 252)
    pdf.cell(0, 6, f"  Age: {age}  |  Blood Pressure: {bp} mmHg  |  Cholesterol: {chol} mg/dL", ln=True)
    pdf.cell(0, 6, f"  Heart Rate: {hr} bpm  |  ST Depression: {oldpeak}", ln=True)
    pdf.ln(2)

    pdf.signature_block()

    fname = os.path.join(out_dir, f"heart_report_{idx+1:02d}_{pt[0].split()[0].lower()}.pdf")
    pdf.output(fname)
    print(f"  OK  {fname}")


