"""
generate_test_reports.py
========================
Generates 10 realistic hospital-style PDF diagnostic reports
(5 Heart Disease, 5 Diabetes) saved in:
    testing_data/heart/
    testing_data/diabetes/

Each PDF is formatted like a real diagnostic lab report and
is fully parseable by the MedicalAI application.

Required values parsed by the app:
  Heart    -> Age, Blood Pressure, Cholesterol, Heart Rate, ST Depression
  Diabetes -> Pregnancies, Glucose, Blood Pressure, Skin Thickness,
              Insulin, BMI, Pedigree, Age

Run from BIO_MODULE directory:
    pip install fpdf2
    python generate_test_reports.py
"""

