def sample_date():
    d = date(2024, random.randint(1, 12), random.randint(1, 28))
    return d.strftime("%d %b %Y")


# ---- PDF base class ----------------------------------------------------------
class LabReport(FPDF):
    def __init__(self, title, ref, patient, doctor, accent):
        super().__init__()
        self.title_text  = title
        self.ref_no      = ref
        self.patient     = patient   # (name, gender, age, pid)
        self.doctor      = doctor    # (name, qual, reg)
        self.accent      = accent    # (R,G,B)
        self.rpt_date    = sample_date()
        self.set_margins(14, 14, 14)
        self.set_auto_page_break(auto=True, margin=14)

    # header -------------------------------------------------------------------
    def header(self):
        self.set_fill_color(*NAVY)
        self.rect(0, 0, 210, 22, "F")
        self.set_font("Helvetica", "B", 14)
        self.set_text_color(*WHITE)
        self.set_y(5)
        self.cell(0, 12, LAB["name"], align="C")

        self.set_fill_color(*self.accent)
        self.rect(0, 22, 210, 7, "F")
        self.set_font("Helvetica", "", 8)
        self.set_y(23.5)
        self.cell(0, 5, LAB["nabl"], align="C")

        self.set_text_color(*BLACK)
        self.ln(12)

    # footer -------------------------------------------------------------------
    def footer(self):
        self.set_y(-14)
        self.set_draw_color(*self.accent)
        self.set_line_width(0.4)
        self.line(14, self.get_y(), 196, self.get_y())
        self.set_font("Helvetica", "I", 7.5)
        self.set_text_color(*GREY)
        self.cell(0, 7,
            "This report is generated by a certified laboratory. "
            "Results should be interpreted by a qualified physician.  "
            f"{LAB['phone']}  |  {LAB['addr']}",
            align="C")

    # patient info block -------------------------------------------------------
    def patient_block(self):
        name, gender, age, pid = self.patient
        doc_name, _, _ = self.doctor
        self.set_fill_color(*LGREY)
        self.set_draw_color(*self.accent)
        self.set_line_width(0.3)
        y0 = self.get_y()
        self.rect(14, y0, 182, 28, "FD")

        def kv(lx, ly, key, val):
            self.set_font("Helvetica", "B", 9)
            self.set_text_color(*NAVY)
            self.set_xy(lx, ly)
            self.cell(44, 5, key)
            self.set_font("Helvetica", "", 9)
            self.set_text_color(*BLACK)
            self.cell(50, 5, val)

        kv(18, y0+3,  "Patient Name :",  name)
        kv(18, y0+10, "Patient ID :",    pid)
        kv(18, y0+17, "Age / Gender :",  f"{age} Years  /  {gender}")
        kv(115, y0+3,  "Report Date :",  self.rpt_date)
        kv(115, y0+10, "Ref. No. :",     self.ref_no)
        kv(115, y0+17, "Physician :",    doc_name)
        self.ln(32)

    # section heading ----------------------------------------------------------
    def section_heading(self, text):
        self.set_fill_color(*self.accent)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 10)
        self.cell(0, 7, "  " + text, fill=True, ln=True)
        self.set_text_color(*BLACK)
        self.ln(1)

    # table header -------------------------------------------------------------
    def tbl_header(self, cols, widths):
        self.set_fill_color(*NAVY)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 9)
        for c, w in zip(cols, widths):
            self.cell(w, 7, c, border=0, fill=True, align="C")
        self.ln()
        self.set_text_color(*BLACK)

    # table row ----------------------------------------------------------------
    def tbl_row(self, vals, widths, flag=None):
        bg = (255, 245, 245) if flag == "H" else (245, 248, 255) if flag == "L" else WHITE
        self.set_fill_color(*bg)
        self.set_font("Helvetica", "", 9)
        for i, (v, w) in enumerate(zip(vals, widths)):
            pad   = "  " if i == 0 else ""
            align = "L" if i == 0 else "C"
            self.cell(w, 7, pad + str(v), border="B", fill=True, align=align)
        if flag:
            self.set_text_color(*RED)
            self.set_font("Helvetica", "B", 9)
            self.cell(8, 7, flag, border="B", align="C")
            self.set_text_color(*BLACK)
        else:
            self.cell(8, 7, "", border="B")
        self.ln()

    # interpretation box -------------------------------------------------------
    def interpretation(self, text, risk):
        col_map = {
            "High": (200, 50, 50), "Moderate": (200, 130, 0), "Normal": (30, 140, 60)
        }
        col = col_map.get(risk, GREY)
        self.ln(4)
        self.set_fill_color(*col)
        self.set_text_color(*WHITE)
        self.set_font("Helvetica", "B", 9)
        self.cell(0, 7, f"  INTERPRETATION  |  Risk Level: {risk.upper()}", fill=True, ln=True)
        self.set_fill_color(250, 250, 255)
        self.set_text_color(*BLACK)
        self.set_font("Helvetica", "", 9)
        self.multi_cell(0, 6, "  " + text, border="LRB", fill=True)
        self.ln(3)

    # doctor signature ---------------------------------------------------------
    def signature_block(self):
        self.ln(8)
        doc_name, doc_qual, doc_reg = self.doctor
        self.set_draw_color(*NAVY)
        self.set_line_width(0.3)
        x = 120
        self.line(x, self.get_y(), 196, self.get_y())
        self.set_font("Helvetica", "B", 9)
        self.set_xy(x, self.get_y() + 1)
        self.cell(76, 5, doc_name, align="C", ln=True)
        self.set_font("Helvetica", "", 8)
        self.set_text_color(*GREY)
        self.set_x(x); self.cell(76, 4, doc_qual, align="C", ln=True)
        self.set_x(x); self.cell(76, 4, doc_reg,  align="C", ln=True)
        self.set_text_color(*BLACK)

    # report title -------------------------------------------------------------
    def report_title(self):
        self.set_font("Helvetica", "B", 13)
        self.set_text_color(*NAVY)
        self.cell(0, 8, self.title_text, align="C", ln=True)
        self.set_draw_color(*self.accent)
        self.set_line_width(0.5)
        self.line(14, self.get_y(), 196, self.get_y())
        self.set_text_color(*BLACK)
        self.ln(4)

    # info note box ------------------------------------------------------------
    def info_note(self, text, bg, tc, bc):
        self.set_fill_color(*bg)
        self.set_draw_color(*bc)
        self.set_line_width(0.3)
        self.set_font("Helvetica", "B", 8.5)
        self.set_text_color(*tc)
        self.cell(0, 6, "  [i]  " + text, border=1, fill=True, ln=True)
        self.set_text_color(*BLACK)
        self.ln(3)


